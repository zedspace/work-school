package claseResurse;

public class Grupa {
	private int id_grupa;
	private int numar_grupa;
	private int an_studiu;
	private int numar_studenti;
	private int specializare_cod_specializare;
	public int getId_grupa() {
		return id_grupa;
	}
	public void setId_grupa(int id_grupa) {
		this.id_grupa = id_grupa;
	}
	public int getNumar_grupa() {
		return numar_grupa;
	}
	public void setNumar_grupa(int numar_grupa) {
		this.numar_grupa = numar_grupa;
	}
	public int getAn_studiu() {
		return an_studiu;
	}
	public void setAn_studiu(int an_studiu) {
		this.an_studiu = an_studiu;
	}
	public int getNumar_studenti() {
		return numar_studenti;
	}
	public void setNumar_studenti(int numar_studenti) {
		this.numar_studenti = numar_studenti;
	}
	public int getSpecializare_cod_specializare() {
		return specializare_cod_specializare;
	}
	public void setSpecializare_cod_specializare(int specializare_cod_specializare) {
		this.specializare_cod_specializare = specializare_cod_specializare;
	}
	
	

}
