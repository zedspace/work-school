package claseResurse;

public class Disciplina {

	private int id_disciplina;
	private int cod_disciplina;
	private String tip_disciplina;
	private String denumire_disciplina;
	public int getId_disciplina() {
		return id_disciplina;
	}
	public void setId_disciplina(int id_disciplina) {
		this.id_disciplina = id_disciplina;
	}
	public int getCod_disciplina() {
		return cod_disciplina;
	}
	public void setCod_disciplina(int cod_disciplina) {
		this.cod_disciplina = cod_disciplina;
	}
	public String getTip_disciplina() {
		return tip_disciplina;
	}
	public void setTip_disciplina(String tip_disciplina) {
		this.tip_disciplina = tip_disciplina;
	}
	public String getDenumire_disciplina() {
		return denumire_disciplina;
	}
	public void setDenumire_disciplina(String denumire_disciplina) {
		this.denumire_disciplina = denumire_disciplina;
	}
	
}
