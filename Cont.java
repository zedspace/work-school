package claseResurse;

public class Cont {

	private String utilizator;
	private String parola;
	private String informatii;
	private int marca;
	private int numar_matricol;
	public String getUtilizator() {
		return utilizator;
	}
	public void setUtilizator(String utilizator) {
		this.utilizator = utilizator;
	}
	public String getParola() {
		return parola;
	}
	public void setParola(String parola) {
		this.parola = parola;
	}
	public String getInformatii() {
		return informatii;
	}
	public void setInformatii(String informatii) {
		this.informatii = informatii;
	}
	public int getMarca() {
		return marca;
	}
	public void setMarca(int marca) {
		this.marca = marca;
	}
	public int getNumar_matricol() {
		return numar_matricol;
	}
	public void setNumar_matricol(int numar_matricol) {
		this.numar_matricol = numar_matricol;
	}
	
}
