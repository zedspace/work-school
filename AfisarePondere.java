package claseResurse;

public class AfisarePondere {
	private String numeProfesor;
	private String prenumeProfesor;
	private String titulatura;
	private String numeDisciplina;
	private String tipDisciplina;
	private String an;
	private String specializare;
	private int grupa;
	private int pondere;
	public String getNumeProfesor() {
		return numeProfesor;
	}
	public void setNumeProfesor(String numeProfesor) {
		this.numeProfesor = numeProfesor;
	}
	public String getPrenumeProfesor() {
		return prenumeProfesor;
	}
	public void setPrenumeProfesor(String prenumeProfesor) {
		this.prenumeProfesor = prenumeProfesor;
	}
	public String getTitulatura() {
		return titulatura;
	}
	public void setTitulatura(String titulatura) {
		this.titulatura = titulatura;
	}
	public String getNumeDisciplina() {
		return numeDisciplina;
	}
	public void setNumeDisciplina(String numeDisciplina) {
		this.numeDisciplina = numeDisciplina;
	}
	public String getTipDisciplina() {
		return tipDisciplina;
	}
	public void setTipDisciplina(String tipDisciplina) {
		this.tipDisciplina = tipDisciplina;
	}
	public String getAn() {
		return an;
	}
	public void setAn(String an) {
		this.an = an;
	}
	public String getSpecializare() {
		return specializare;
	}
	public void setSpecializare(String specializare) {
		this.specializare = specializare;
	}
	public int getGrupa() {
		return grupa;
	}
	public void setGrupa(int grupa) {
		this.grupa = grupa;
	}
	public int getPondere() {
		return pondere;
	}
	public void setPondere(int pondere) {
		this.pondere = pondere;
	}
	

}
