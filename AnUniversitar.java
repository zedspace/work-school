package claseResurse;

public class AnUniversitar {
	 
	private int id_an_universitar;
	private String denumire_an_universitar;
	private int semestrul;
	public int getId_an_universitar() {
		return id_an_universitar;
	}
	public void setId_an_universitar(int id_an_universitar) {
		this.id_an_universitar = id_an_universitar;
	}
	public String getDenumire_an_universitar() {
		return denumire_an_universitar;
	}
	public void setDenumire_an_universitar(String denumire_an_universitar) {
		this.denumire_an_universitar = denumire_an_universitar;
	}
	public int getSemestrul() {
		return semestrul;
	}
	public void setSemestrul(int semestrul) {
		this.semestrul = semestrul;
	}
	

}
