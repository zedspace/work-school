package claseResurse;

public class Departament {

	private int cod_departament;
	private String denumire_departament;
	public int getCod_departament() {
		return cod_departament;
	}
	public void setCod_departament(int cod_departament) {
		this.cod_departament = cod_departament;
	}
	public String getDenumire_departament() {
		return denumire_departament;
	}
	public void setDenumire_departament(String denumire_departament) {
		this.denumire_departament = denumire_departament;
	}
	
}