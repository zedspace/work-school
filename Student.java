
package claseResurse;

public class Student {

	private int numar_matricol;
	private String cnp;
	private String nume;
	private String prenume;
	private String forma_finantare;
	public int getNumar_matricol() {
		return numar_matricol;
	}
	public void setNumar_matricol(int numar_matricol) {
		this.numar_matricol = numar_matricol;
	}
	public String getCnp() {
		return cnp;
	}
	public void setCnp(String cnp) {
		this.cnp = cnp;
	}
	public String getNume() {
		return nume;
	}
	public void setNume(String nume) {
		this.nume = nume;
	}
	public String getPrenume() {
		return prenume;
	}
	public void setPrenume(String prenume) {
		this.prenume = prenume;
	}
	public String getForma_finantare() {
		return forma_finantare;
	}
	public void setForma_finantare(String forma_finantare) {
		this.forma_finantare = forma_finantare;
	}

	
}
