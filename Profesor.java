package claseResurse;

public class Profesor {
	
	private int marca;
	private String nume;
	private String prenume;
	private String titulatura;
	private int cod_departament;
	private String nume_utilizator;
	public int getMarca() {
		return marca;
	}
	public void setMarca(int marca) {
		this.marca = marca;
	}
	public String getNume() {
		return nume;
	}
	public void setNume(String nume) {
		this.nume = nume;
	}
	public String getPrenume() {
		return prenume;
	}
	public void setPrenume(String prenume) {
		this.prenume = prenume;
	}
	public String getTitulatura() {
		return titulatura;
	}
	public void setTitulatura(String titulatura) {
		this.titulatura = titulatura;
	}
	public int getCod_departament() {
		return cod_departament;
	}
	public void setCod_departament(int cod_departament) {
		this.cod_departament = cod_departament;
	}
	public String getNume_utilizator() {
		return nume_utilizator;
	}
	public void setNume_utilizator(String nume_utilizator) {
		this.nume_utilizator = nume_utilizator;
	}
	
	

}
